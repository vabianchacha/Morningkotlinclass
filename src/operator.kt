fun main() {
//Arithmetic Operators
    var a = 10
    var b = 5
    println(a+b)

    //comparison

    var x=89
    var y=100
    println(x<y)
    println(x>y)
    println(x>=y)
    println(x<=y)
    println(x==y)
    println(x>y)


    //assignment operators
    var  number = 26
    println(number)

    number += 2
    println(number)


    var result = 5 + (10*2)- 3

    println("result is $result")

    //logical Operators -&&,||, !, -not
    var num = 56
    println(num >100 && num < 80)
    println(num>100 || num <80)
    println(!(num>100 || num <80))


}